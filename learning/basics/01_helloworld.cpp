#include <iostream> // for input and output

int main() {
    std::cout << "Hello World!" << std::endl; // std::endl clears the output buffer as well as shifts to next line
    std::cout << "The weather is good!" << '\n'; // \n also shifts to next line
    std::cout << "Life is good sometimes." << '\n';

    return 0;
}

